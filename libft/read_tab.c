#include "libft.h"

void		read_tab(char **tab)
{
	int i;

	i = 0;
	while (tab[i] != NULL)
	{
		ft_putstr(tab[i]);
		write(1, " ", 1);
		i++;
	}
	write(1, "\n", 1);
}
